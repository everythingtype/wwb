<div class="row">
<div class="four columns">
<h4>Research:</h4>
<h5>Reaching more women</h5>

<div class="share-bar">

<div class="share-icons">
<!-- Twitter -->
<a href="https://twitter.com/home?status=We%20research%20the%20needs%20of%20low-income%20women%20so%20we%20can%20%23BankOnHer%20via%20%40womensworldbnkg" target="_blank">
<img src="images/share-twitter.jpg" alt="Share on Twitter" />
</a>
<!-- Facebook -->
<a href="https://www.facebook.com/sharer/sharer.php?u=http://womensworldbanking.org/annualreport2014" target="_blank"><img src="images/share-facebook.jpg" alt="Share on Facebook" /></a>
</div>
</div>


</div>
<div class="eight columns">
<h6>At the heart of Women’s World Banking’s work is the commitment to ensure that the largest number of low-income women is served well with innovative financial products and services. Our research team leads the effort to better understand the needs of the more than one billion women worldwide who remain outside the formal financial system. In 2014, our research expertise helped us identify new markets and new segments of unbanked women, including:</h6>
</div>
</div>

<div class="row">
<div class="four columns only-desktop">
<img class="left-desktop" src="images/research-image.jpg" alt="">
</div>
<div class="eight columns">
<p><strong>Low-income salaried women who are employed cleaning houses, working in factories and hotels.</strong> Women’s World Banking’s research team visited Bangalore and Delhi, India to determine how to close the financial inclusion gap for garment factory employees. The research revealed that women factory workers, despite having their salaries deposited directly into a “no-frills” account, had little control over the account. Only 34 percent had sole ownership of their accounts, compared to 91 percent of men, and 57 percent of women handed their incomes over to someone else, compared to 12 percent of men. Women’s World Banking will move this research into action through partnerships to provide financial education and products for these women.</p>
<p><a class="blue" href="http://www.womensworldbanking.org/news/blog/when-can-we-start-the-financial-education-needs-of-low-income-garment-factory-employees-in-india/" target="_blank">Read more on our blog</a></p>

<p><strong>Women who own small- and medium-sized enterprises (SMEs)</strong>. Full financial inclusion for women means access to a variety of appropriate financial products and services to meet their diverse needs. A projected 48 million female entrepreneurs and 64 million female business owners currently employ one or more people in their businesses. It is estimated that over 70% of these women-led enterprises are either un-served or underserved financially. In fact, the credit gap for these companies is estimated at over $300 billion. Women’s World Banking conducted research to identify best practices among banks that are successfully serving this market.<p>
<p><a class="blue" href="http://www.womensworldbanking.org/news/blog/six-best-practices-best-serve-women-led-smes/" target="_blank">Read more on our blog</a></p>

<p><strong>Low-income women affected by natural disasters.</strong> When natural disasters strike, low-income women feel the impact acutely. What can financial institutions do to support their clients and the community in crisis? What services do clients need? To answer these questions, Women’s World Banking began research in 2014, interviewing eight network institutions that suffered natural disasters or political unrest. We studied a range of approaches—from financial offerings such as bridge loans and expedited insurance payments to non-financial offerings including relief packages and child care during rebuilding. Best practices emerged in many institutions, for example Negros Women for Tomorrow Foundation in the Philippines, which sets aside budget each year for natural disasters like the 2013 typhoon Yolanda. This institution has also developed partnerships with the private sector and NGOs to help clients rebuild after a disaster. </p>

<img class="left-mobile" src="images/research-image.jpg" alt="">
</div>
</div>
</div>
