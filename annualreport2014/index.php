<div id="digital-savings" class="tab">
<div class="row">
<div class="four columns">
<h4>Digital Savings: </h4>
<h5>Reaching women <br>where they are</h5>

<div class="share-bar">

<div class="share-icons">
<!-- Twitter -->
<a href="https://twitter.com/home?status=How%20%40womensworldbnkg%20helps%20bring%20the%20bank%20to%20low-income%20women%20through%20agents%20%23BankOnHer" target="_blank">
<img src="images/share-twitter.jpg" alt="Share on Twitter" />
</a>
<!-- Facebook -->
<a href="https://www.facebook.com/sharer/sharer.php?u=http://womensworldbanking.org/annualreport2014" target="_blank"><img src="images/share-facebook.jpg" alt="Share on Facebook" /></a>
</div>
</div>


</div>
<div class="eight columns">
<h6>Many of the barriers that unbanked women face will sound familiar—lack of decision-making power within their families, lack of education, lack of formal employment. We see these barriers to women's equality all around the world. However, two barriers to financial inclusion may be less obvious: time and distance. Take two examples:</h6>
</div>
</div>

<div class="row">
<div class="four columns only-desktop">
<img class="left-desktop" src="images/digital-savings-img.jpg" alt="">
</div>
<div class="eight columns">
<p><strong>1. A rural woman in Malawi needs to travel long distances to reach the nearest bank.</strong> The amount spent on bus fare as well as the time spent away from her work or family make it impractical to open a bank account and establish a safe place to save her money. </p>

<p><strong>2. An urban woman in Nigeria can’t afford to take the time away from her busy market stall to visit a nearby bank branch.</strong> She doesn’t see the bank as being “for her” and faces great risk carrying her earnings home each night.</p>

<p>Women’s World Banking is working with two commercial banks to break down these barriers by delivering products through a new channel: digital technology. We worked with Diamond Bank in Nigeria to create <a href="http://www.womensworldbanking.org/news/blog/diamond-bank-storms-market-beta-way-save/" target="_blank">BETA Savings</a>, a savings account designed for women and men who run stalls in the open-air markets. Agents, knowns as BETA Friends, visit a client’s business to open accounts digitally and handle deposits and withdrawals using mobile technology. By the end of 2014, more than 154,000 accounts were opened, primarily from clients with no prior relationship with a formal financial institution.</p>

<p>In Malawi, NBS Bank worked with Women's World Banking to develop the <a href="http://www.womensworldbanking.org/news/news-news/huffington-post-innovative-savings-accounts-can-help-low-income-women/" target="_blank">Pafupi</a> (meaning “close” in the local language of Chichewa) savings account, a digital account designed for low-income rural women with no previous access to a bank. Women can open accounts and make small deposits and withdrawals whenever they want at local shops with NBS Bank agents using mobile technology.</p>

<p>These digital savings products not only help women overcome time and distance barriers, they also offer a step toward economic empowerment. For many women, having a savings account is the first point of access to the formal economy. With digital technology, a woman, who may not have otherwise had access, can open a savings account in her own name. She can plan for her future and the future of her family, build a safety net for times of crisis, and even grow her business with access to loans and other offerings from the bank. </p>

<img class="left-mobile" src="images/digital-savings-img.jpg" alt="">
</div>
