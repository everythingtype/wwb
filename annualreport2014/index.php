<p><strong>As conveners.</strong> Only 30 percent of women in sub-Saharan Africa have a bank account. The reasons are many and potential solutions abound. Women’s World Banking has convened 12 high-profile African leaders committed to increasing women’s participation in the formal financial sector to form the <a href="http://www.womensworldbanking.org/news/blog/broadening-horizons-africa-introducing-womens-world-bankings-africa-advisory-council/" target="_blank">Africa Advisory Council</a>. Council members are from diverse backgrounds, geographies and industries, highlighting the wide range of people and organizations it takes to work together to close the financial inclusion gap. Members serve as ambassadors for women’s financial inclusion issues within their personal and professional circles.</p>
<p><a class="blue" href="http://in.reuters.com/article/2014/11/24/africa-women-finance-mobile-idINKCN0J81L920141124" target="_blank">See the article</a></p>

<p><strong>By training mission-focused leaders.</strong> Serving low-income clients sustainably takes dedication and focus. Women’s World Banking has been training leaders of financial institutions to better serve women for more than a decade. Our current Leadership and Diversity for Innovation program helps financial institutions achieve their social and financial goals as they navigate ever-changing and competitive environments. In 2014 we trained senior managers in Nigeria, Bolivia and Colombia and held our signature Women in Leadership Program for high-potential women in Latin America.</p>

<p><strong>As active shareholders.</strong> Financial institutions need capital from investors who understand that investing in “unbanked” women can provide both social and financial returns. <a href="https://www.womensworldbanking.org/about-us/womens-world-banking-capital-partners/" target="_blank">Women’s World Banking Capital Partners</a>, launched in 2012, is a $50 million private equity limited partnership that invests directly in women-focused financial institutions, providing much-needed capital for growth and product expansion, as well as strategic guidance on serving the women’s market.</p>

<p><strong>As thought leaders.</strong> Women’s World Banking was recognized by <a href="http://www.theguardian.com/global-development-professionals-network/2014/sep/25/10-twitter-accounts-to-follow-for-financial-inclusion-news-and-views" target="_blank">The Guardian</a> as one of the top 10 Twitter accounts to follow for financial inclusion. In 2014, Women's World Banking participated in women-focused initiatives of the World Economic Forum, the G20, the Better Than Cash Alliance and many other collaborations. Our staff members were featured speakers at high-profile events and organizations around the world, including the UN Women’s <a href="http://empowerwomen.org" target="_blank">EmpowerWomen.org</a>, <a href="https://www.youtube.com/watch?v=jIudIXstF4c">Trust Women Conference</a>, the Council on Foreign Relations, Mobile Money Americas, the <a href="https://www.youtube.com/watch?v=0ZpIJggOU60" target="_blank">Institute of International Finance</a>, and the Clinton Global Initiative. In November 2015, we will convene our network members, industry leaders and allies from around the world—join us in Berlin for our <strong>Making Finance Work for Women Summit!</strong></p>
