</div>


</div>
<div class="eight columns">
<h6>Monitoring and evaluating our work is fundamental to delivering on our mission. We are constantly studying the impact of our work on women’s financial inclusion at three levels: </h6>
</div>
</div>

<div class="row">
<div class="four columns only-desktop">
<img class="left-desktop" src="images/monitoring-img.jpg" alt="">
</div>
<div class="eight columns">
<p><strong>The client.</strong> Are clients accessing financial products and services that meet their needs and improve their lives? </p>

<p><strong>The financial institution.</strong> Are we developing commercially viable products that contribute to an institution’s financial sustainability and increasing its capacity to serve low-income women? </p>

<p><strong>The industry.</strong> Are we fostering a greater understanding of the financial needs of low-income women, knowledge about how to serve this market well, and an evidence base demonstrating both the financial and social impact of serving low-income women?</p>

<p>Women’s World Banking’s <a href="http://www.womensworldbanking.org/research/gender-performance-initiative/interactive-select-five/" target="_blank">Gender Performance Indicators</a> are the first metrics to help financial institutions measure how well they are serving women. A select group of five indicators are used by our network institutions to measure their success in serving women clients, as well as promoting women leaders within their institutions. </p>

<p>We are also returning to institutions where we introduced products to assess the impact of those products. Four years after collaborating with Banco ADOPEM in the Dominican Republic to develop a youth savings program called the Mía account, Women’s World Banking returned to the bank to evaluate the impact of the program and assess lessons learned. Data gathered through this exercise informed our understanding of the keys to ensuring sustainability for a financial institution’s youth savings program, particularly those serving the low-income market:</p>
<p>
<strong>1.</strong> Offer products for both youth and parents <br>
<strong>2.</strong> Design products without dormancy fees so small balances can grow <br>
<strong>3.</strong> Establish a retention plan to ensure youth stay with the bank<br>
<strong>4.</strong> Invest in marketing and financial education<br>
<strong>5.</strong> Ensure products are financially sustainable</p>

<p><a href="https://www.womensworldbanking.org/research/gender-performance-initiative/" class="blue" target="_blank">Read the report</a>
</p>

