<div class="row">
<div class="four columns">
<h4>Rural Women:</h4>
<h5>Serving an unrecognized market</h5>

<div class="share-bar">

<div class="share-icons">
<!-- Twitter -->
<a href="https://twitter.com/home?status=Rural%20women%20contribute%20so%20much%20to%20the%20household%20but%20are%20often%20invisible%20%23BankOnHer%20via%20%40womensworldbnkg" target="_blank">
<img src="images/share-twitter.jpg" alt="Share on Twitter" />
</a>
<!-- Facebook -->
<a href="https://www.facebook.com/sharer/sharer.php?u=http://womensworldbanking.org/annualreport2014" target="_blank"><img src="images/share-facebook.jpg" alt="Share on Facebook" /></a>
</div>
</div>


</div>
<div class="eight columns">
<h6>Rural women, perhaps more so than their urban counterparts, fulfill a wide range of roles in a household. They serve as the primary caregiver, both to children and the elderly and take responsibility for the family’s housing, healthcare and education as well. They play the role of farm hand for large crops while also tending their own small livestock (chickens for eggs, goats for milk and cheese). </h6>
</div>
</div>

<div class="row">
<div class="four columns only-desktop">
<img class="left-desktop" src="images/rurual-women-img.jpg" alt="">
</div>
<div class="eight columns">
<p>These women may also have other income-generating activities such as running a small shop or making and selling food. Although their financial contributions to the household are important, women rarely perceive them as such; they describe their activities as simply part of being a housewife. However, these women are valuable, unserved potential clients for a bank.
</p>
<p>Women’s World Banking concluded a three-year project in 2014 to develop financial products for rural women in Latin America. We partnered with three network members: Interfisa Banco (Paraguay), Fundación delamujer (Colombia), and Caja Arequipa (Peru), each of which saw a growth opportunity in serving this untapped market segment. While all of these institutions had experience offering loans to entrepreneurs, most had developed that expertise among an urban clientele and employed the same lending methodology to their rural clients. They found that rural repayment rates were poor, clients were leaving after their first loans and women made up a much smaller part of the portfolio than in urban markets. Based on our customer research we were able to modify loans to ensure that they were appropriate for financing women’s rural businesses—the right amount, with repayment dates that match income and without onerous collateral or co-signatory requirements. As a result, more than 43,000 new loans have been issued to women—half of whom were new clients for the banks. Together these women are using this $45 million of new capital to grow their businesses and contribute to the well-being of their families.
</p>

<img class="left-mobile" src="images/rurual-women-img.jpg" alt="">
<p><a class="blue" href="http://www.womensworldbanking.org/news/blog/giving-credit-rural-women-developing-inclusive-finance-latin-america/" target="_blank">Read our blog</a></p>
</div>
